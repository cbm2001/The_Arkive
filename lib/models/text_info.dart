import 'package:flutter/material.dart';

class TextInfo {
  String text;
  double left;
  double top;
  Color color;
  FontWeight fontWeight;
  FontStyle fontStyle;
  double fontSize;
  TextAlign textAlign;

  TextInfo({
    this.text,
    this.left,
    this.top,
    this.color,
    this.fontWeight,
    this.fontStyle,
    this.fontSize,
    this.textAlign,
  });
}


